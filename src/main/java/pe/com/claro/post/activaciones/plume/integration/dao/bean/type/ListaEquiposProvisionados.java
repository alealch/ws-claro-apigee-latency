package pe.com.claro.post.activaciones.plume.integration.dao.bean.type;

public class ListaEquiposProvisionados {
  private String codigoCliPLM;
  private String codigoSucPLM;
  private String serialNumber;
  private String nickName;
  private String modelo;
  private String codSeriePLM;

  public String getCodigoCliPLM() {
    return codigoCliPLM;
  }

  public void setCodigoCliPLM(String codigoCliPLM) {
    this.codigoCliPLM = codigoCliPLM;
  }

  public String getCodigoSucPLM() {
    return codigoSucPLM;
  }

  public void setCodigoSucPLM(String codigoSucPLM) {
    this.codigoSucPLM = codigoSucPLM;
  }

  public String getSerialNumber() {
    return serialNumber;
  }

  public void setSerialNumber(String serialNumber) {
    this.serialNumber = serialNumber;
  }

  public String getNickName() {
    return nickName;
  }

  public void setNickName(String nickName) {
    this.nickName = nickName;
  }

  public String getModelo() {
    return modelo;
  }

  public void setModelo(String modelo) {
    this.modelo = modelo;
  }

  public String getCodSeriePLM() {
    return codSeriePLM;
  }

  public void setCodSeriePLM(String codSeriePLM) {
    this.codSeriePLM = codSeriePLM;
  }

}
