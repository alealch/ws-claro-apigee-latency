package pe.com.claro.post.activaciones.plume.canonical.type;

public class ListaLocacionesOLType {
  private String codigoSucursal;
  private String fechaCreacion;
  private String id;
  private String nivelServicio;
  private String nombre;

  public String getCodigoSucursal() {
    return codigoSucursal;
  }

  public void setCodigoSucursal(String codigoSucursal) {
    this.codigoSucursal = codigoSucursal;
  }

  public String getFechaCreacion() {
    return fechaCreacion;
  }

  public void setFechaCreacion(String fechaCreacion) {
    this.fechaCreacion = fechaCreacion;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getNivelServicio() {
    return nivelServicio;
  }

  public void setNivelServicio(String nivelServicio) {
    this.nivelServicio = nivelServicio;
  }

  public String getNombre() {
    return nombre;
  }

  public void setNombre(String nombre) {
    this.nombre = nombre;
  }

}
